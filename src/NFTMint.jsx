const web3 = new Web3(new Web3.providers.HttpProvider('http://localhost:8545'));

const contract = new web3.eth.Contract(abi, contractAddress);


function NFTMint({ mint, onClose, isLog, meta }) {
    const [tokenId, setTokenId] = useState("");
    const [tokenUri, setTokenUri] = useState("");
    console.log('Inside NFTMint.jsx file - ', isLog)
    const contractAddress = "0xc0ED63E3A70BfCB003452B1Cc083db822e1f23e1";
    const startMint = async () => {
        
        const mintFunction = contract.methods.mint(meta.accounts[0], tokenId, tokenUri);
        const encodedABI = mintFunction.encodeABI();
        const nonce = await web3.eth.getTransactionCount(meta.accounts[0]);
        console.log(meta.accounts[0]);
        console.log(nonce)
        console.log(tokenId)
        console.log(tokenUri)
        window.ethereum.request({
            method: 'eth_sendTranscations',
            params: [{
                from: meta.accounts[0],
                to: contractAddress,
                nonce: nonce,
                gas: '0x521',
                gasPrice: '0x4A817C8',
                gasLimit: web3.utils.toHex(16234000),
                data: encodedABI
            },
            ],
        })
        .then((txHash) => console.log(txHash))
        .catch((error) => console.log(error));
    }
    return (
        <Dialog open={mint} onClose={onClose} fullWidth>

            {isLog ? (
                <>
                    <DialogTitle align="center">Fill the following areas</DialogTitle>
                    <DialogContent>
                        <TextField
                            required
                            margin="dense"
                            id="tokenId"
                            label="Token ID"
                            fullWidth
                            variant="standard"
                            onChange={(e) => setTokenId(e.target.value)}
                        />
                        <TextField
                            required
                            margin="dense"
                            id="uri"
                            label="Token URI"
                            fullWidth
                            variant="standard"
                            onChange={(e) => setTokenUri(e.target.value)}
                        />
                    </DialogContent>
                    <DialogActions style={{ justifyContent: 'center' }}>
                        <Button variant="contained" onClick={startMint}>Mint</Button>
                    </DialogActions>
                </>
            ) : (
                <DialogTitle align="center">Your wallet is not connected. Please connect your wallet at first.</DialogTitle>
            )}

        </Dialog>
    );
}

export default NFTMint;