const contractAddress = '0xA9F8FeF0B3DF9159F1443427dAa79210fCEB009C';
const contract = new web3.eth.Contract(abi, contractAddress);

export async function mint(toAddress, uri, tokenId, privateKey) {
    const nonce = await web3.eth.getTransactionCount(toAddress);

    const mintFunction = contract.methods.mint(toAddress, tokenId, uri);
    const encodedABI = mintFunction.encodeABI();

    //const gasPrice = await web3.eth.getGasPrice();

    const tx = {
        from: toAddress,
        to: contractAddress,
        nonce: nonce,
        gas: '0x521',
        gasPrice: '0x4A817C8',
        gasLimit: web3.utils.toHex(16234000),
        data: encodedABI
    };

    const signPromise = web3.eth.accounts.signTransaction(tx, privateKey);

    signPromise.then((signedTx) => {

        web3.eth.sendSignedTransaction(signedTx.rawTransaction, function (err, hash) {
            if (!err) {
                console.log(`The hash of your mint transaction is: ${hash}`);
            } else {
                console.log("Error message " + err)
            }
        });
    }).catch((err) => {
        console.log("Promise failed:", err);
    });
}
